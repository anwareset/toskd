-- Jalankan seluruh query ini di Supabase SQL Editor

-- 1. Tabel Bank Soal (questions)
CREATE TABLE IF NOT EXISTS questions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    content TEXT NOT NULL,              -- Konten soal (HTML, termasuk gambar inline)
    question_type TEXT NOT NULL DEFAULT 'text', -- Tipe soal (selalu 'text' karena gambar inline di rich text editor)
    options JSONB NOT NULL,             -- {"A": "...", "B": "...", "C": "...", "D": "...", "E": "..."}
    correct_answer VARCHAR(1) NOT NULL, -- 'A', 'B', 'C', 'D', 'E'
    explanation TEXT NOT NULL,          -- Pembahasan (HTML, termasuk gambar inline)
    image_url TEXT,                     -- Deprecated: gambar sekarang inline di content
    explanation_image_url TEXT,         -- Deprecated: gambar sekarang inline di explanation
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 2. Tabel Paket Soal (question_packs)
CREATE TABLE IF NOT EXISTS question_packs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    duration_minutes INTEGER NOT NULL,
    passing_grade INTEGER NOT NULL DEFAULT 85, -- Nilai absolut (contoh: 85 poin)
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 3. Tabel Relasi Paket & Soal (pack_questions)
CREATE TABLE IF NOT EXISTS pack_questions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    pack_id BIGINT REFERENCES question_packs(id) ON DELETE CASCADE NOT NULL,
    question_id BIGINT REFERENCES questions(id) ON DELETE CASCADE NOT NULL,
    question_number INTEGER NOT NULL,
    UNIQUE(pack_id, question_id)
);

-- 4. Tabel Hasil Ujian (exam_results)
CREATE TABLE IF NOT EXISTS exam_results (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    pack_id BIGINT REFERENCES question_packs(id) ON DELETE CASCADE NOT NULL,
    participant_name TEXT NOT NULL,
    score INTEGER NOT NULL,             -- Skor absolut (bukan persentase)
    status TEXT NOT NULL,               -- 'Lulus PG' atau 'Tidak Lulus PG'
    answers JSONB NOT NULL DEFAULT '{}'::jsonb,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- ============================================
-- TABEL ADMIN AUTH (CMS protection)
-- ============================================
-- Stores admin user credentials for CMS access (bank-soal, kelola-soal,
-- paket-soal, paket-detail). Password stored as bcrypt hash (cost 10).
-- Bootstrap: insert via env vars on first cold-start (see src/server.js).
-- Recovery: if password lost, TRUNCATE this table + redeploy with
-- BOOTSTRAP_ADMIN_* env vars to recreate.

CREATE TABLE IF NOT EXISTS admins (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR(64) NOT NULL UNIQUE,       -- UNIQUE untuk race-safe bootstrap
    password_hash VARCHAR(255) NOT NULL,        -- bcrypt hash (cost 10, ~60 chars)
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    last_login_at TIMESTAMP WITH TIME ZONE      -- nullable; updated on each successful login
);

-- Index untuk fast username lookup di login endpoint
CREATE INDEX IF NOT EXISTS idx_admins_username ON admins(username);

